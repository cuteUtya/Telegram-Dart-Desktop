class _EmojiInputPanelState extends State<EmojiInputPanel> {
  Color get _baseColor => ClientTheme.currentTheme.getField("EmojiInputPanelBackgroundColor");

  int currentPanel = 1;
  PageController controller = PageController();
  Map<String, AnimatedEmoji> _curr = {};

  @override
  Widget build(BuildContext context) {
    int maxEmoji = MediaQuery.of(context).size.width ~/ 300;
    return Container(
      height: MediaQuery.of(context).size.height / 3,
      width: maxEmoji * 64,
      decoration: BoxDecoration(
          color: _baseColor,
          borderRadius: const BorderRadius.all(
            Radius.circular(
              8,
            ),
          )),
      child: Padding(
        padding: const EdgeInsets.symmetric(horizontal: 4),
        child: Column(
          children: [
            Expanded(
                child: PageView.builder(
              controller: controller,
              itemCount: EmojiInputPanel.emojiPanelConfig.length,
              itemBuilder: (_, index) => SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: EmojiInputPanel.emojiPanelConfig[currentPanel].groups.map((column) {
                    Widget result;
                    if (column[0] == "/") {
                      result = Text(
                        column.replaceAll("/", ""),
                        style: const TextStyle(color: Colors.white, fontSize: 18),
                      );
                    } else {
                      var emojis = column.split(" ");
                      if (emojis.length > maxEmoji) {
                        List<Widget> cols = [];
                        var colsCount = emojis.length ~/ maxEmoji;
                        if (emojis.length / maxEmoji > 0.0) colsCount++;

                        for (int i = 0; i < colsCount; i++) {
                          var sIndex = i >= colsCount - 1 ? (emojis.length) % maxEmoji : maxEmoji;
                          cols.add(Row(
                            crossAxisAlignment: CrossAxisAlignment.center,
                            mainAxisSize: MainAxisSize.min,
                            children: List.generate(
                              sIndex,
                              (index) => _buildEmoji(
                                emojis[(i * maxEmoji) + index],
                              ),
                            ),
                          ));
                        }
                        result = Column(
                          children: cols,
                          crossAxisAlignment: CrossAxisAlignment.center,
                        );
                      } else {
                        result = Row(
                          mainAxisSize: MainAxisSize.min,
                          mainAxisAlignment: MainAxisAlignment.center,
                          children: emojis
                              .map((row) => _buildEmoji(
                                    row,
                                  ))
                              .toList(),
                        );
                      }
                    }
                    return result;
                  }).toList(),
                ),
              ),
            )),
            Row(
              mainAxisSize: MainAxisSize.min,
              children: EmojiInputPanel.emojiPanelConfig
                  .map(
                    (e) => GestureDetector(
                      onTap: () => controller.animateToPage(
                        EmojiInputPanel.emojiPanelConfig.indexOf(e),
                        duration: Duration(seconds: 1),
                        curve: Curves.ease,
                      ),
                      child: Container(
                        margin: const EdgeInsets.symmetric(
                          horizontal: 4,
                        ),
                        child: Icon(
                          e.icon,
                          size: 36,
                          color: ClientTheme.currentTheme.getField("EmojiInputPanelIconsColor"),
                        ),
                      ),
                    ),
                  )
                  .toList(),
            )
          ],
        ),
      ),
    );
  }

  Widget _buildEmoji(String symbol) {
    if (_curr[symbol] == null) {
      return _Emoji(
        symbol: symbol,
        onLongHover: () async {
          var emoji = await widget.client.send(GetAnimatedEmoji(emoji: symbol));
          if (emoji is! TdError) {
            setState(() {
              _curr[symbol] = emoji as AnimatedEmoji;
            });
          }
        },
      );
    }
    var emojiKey = GlobalKey<WidgetOpacityContollerState>();
    var lottieKey = GlobalKey<WidgetOpacityContollerState>();
    var animDuration = const Duration(milliseconds: 300);
    Future.delayed(
      Duration.zero,
      () {
        emojiKey.currentState?.animateOpacity(0);
        lottieKey.currentState?.animateOpacity(1);
      },
    );
    return MouseRegion(
      onExit: (_) {
        emojiKey.currentState?.animateOpacity(1);
        lottieKey.currentState?.animateOpacity(0);
        Future.delayed(
          animDuration,
          () => setState(
            () => _curr.remove(symbol),
          ),
        );
      },
      child: Stack(
        alignment: Alignment.center,
        children: [
          WidgetOpacityContoller(
            key: emojiKey,
            duration: animDuration,
            child: Text(
              symbol,
              style: TextStyle(
                fontFamily: TextDisplay.getEmojiFont(),
                fontSize: 26,
              ),
            ),
          ),
          WidgetOpacityContoller(
            duration: animDuration,
            key: lottieKey,
            opacity: 0,
            child: Container(
              width: 38,
              height: 38,
              margin: EdgeInsets.symmetric(horizontal: 11.5),
              child: StickerDisplay(
                client: widget.client,
                showOutline: false,
                sticker: _curr[symbol]!.sticker!,
              ),
            ),
          ),
        ],
      ),
    );
  }
}

class _Emoji extends StatefulWidget {
  const _Emoji({
    Key? key,
    required this.symbol,
    required this.onLongHover,
  }) : super(key: key);

  final String symbol;
  final Function() onLongHover;

  @override
  State<_Emoji> createState() => _EmojiState();
}

class _EmojiState extends State<_Emoji> {
  bool isBig = false;
  bool hovered = false;
  @override
  Widget build(BuildContext context) {
    return MouseRegion(
      onEnter: (_) {
        setState(() => hovered = true);
        Future.delayed(
          const Duration(milliseconds: 500),
          () => setState(
            () {
              if (hovered) {
                isBig = true;
                widget.onLongHover();
              }
            },
          ),
        );
      },
      onExit: (_) => setState(() {
        hovered = false;
        Future.delayed(
          const Duration(
            milliseconds: 250,
          ),
          () => setState(
            () {
              isBig = false;
            },
          ),
        );
      }),
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 100),
        decoration: const BoxDecoration(
          borderRadius: BorderRadius.all(
            Radius.circular(
              6,
            ),
          ),
        ),
        margin: const EdgeInsets.symmetric(horizontal: 4),
        padding: EdgeInsets.symmetric(
          horizontal: isBig ? 2 : 10,
          vertical: isBig ? 0 : 10,
        ),
        child: Align(
          alignment: Alignment.center,
          child: AnimatedDefaultTextStyle(
            style: TextStyle(
              fontSize: 26 * (isBig ? 1.5 : 1),
              fontFamily: TextDisplay.getEmojiFont(),
            ),
            duration: const Duration(milliseconds: 100),
            child: Text.rich(
              TextDisplay.parseEmojiInString(
                widget.symbol,
              ),
            ),
          ),
        ),
      ),
    );
  }
}

